def send_investor_email(content, generated_at=None):
    """
    Send investor insights email to recipients configured in .env.

    Required env vars:
        EMAIL_SENDER      – sender Gmail address
        EMAIL_PASSWORD    – Gmail App Password
        EMAIL_RECIPIENTS  – comma-separated recipient addresses

    Optional env vars:
        EMAIL_SMTP_HOST   – default smtp.gmail.com
        EMAIL_SMTP_PORT   – default 587
    """
    smtp_host = os.environ.get('EMAIL_SMTP_HOST', 'smtp.gmail.com')
    smtp_port = int(os.environ.get('EMAIL_SMTP_PORT', '587'))
    sender    = os.environ.get('EMAIL_SENDER', '').strip()
    password  = os.environ.get('EMAIL_PASSWORD', '').strip()
    recip_raw = os.environ.get('EMAIL_RECIPIENTS', '').strip()

    if not sender or not password or not recip_raw:
        msg = "Email not configured – set EMAIL_SENDER, EMAIL_PASSWORD, EMAIL_RECIPIENTS in .env"
        logger.error(msg)
        return False, msg

    recipients = [r.strip() for r in recip_raw.split(',') if r.strip()]
    if not recipients:
        return False, "No valid EMAIL_RECIPIENTS found"

    today   = datetime.now().strftime('%A, %B %d, %Y')
    subject = f"\U0001f4ca Daily Investor Insights – {today}"

    msg = MIMEMultipart('alternative')
    msg['Subject'] = subject
    msg['From']    = f"MyAI Assist <{sender}>"
    msg['To']      = ', '.join(recipients)

    # Plain-text fallback
    msg.attach(MIMEText(f"Daily Investor Insights – {today}\n\n{content}", 'plain', 'utf-8'))
    # HTML version
    msg.attach(MIMEText(build_html_email(content, generated_at), 'html', 'utf-8'))

    try:
        with smtplib.SMTP(smtp_host, smtp_port) as server:
            server.ehlo()
            server.starttls()
            server.login(sender, password)
            server.sendmail(sender, recipients, msg.as_string())
        logger.info(f"Investor insights email sent to: {', '.join(recipients)}")
        return True, f"Email sent to {len(recipients)} recipient(s)"
    except smtplib.SMTPAuthenticationError:
        err = "SMTP authentication failed – check EMAIL_SENDER / EMAIL_PASSWORD"
        logger.error(err)
        return False, err
    except Exception as e:
        logger.error(f"Failed to send email: {e}")
        return False, str(e)
