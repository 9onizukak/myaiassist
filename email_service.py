# -*- coding: utf-8 -*-
import os
import re
import smtplib
import logging
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from datetime import datetime

logger = logging.getLogger(__name__)


def _to_html(content):
    """Convert investor insights markdown-like syntax to HTML body."""
    h = content

    # Headers
    h = re.sub(r'^### (.+)$', r'<h3>\1</h3>', h, flags=re.MULTILINE)
    h = re.sub(r'^## (.+)$', r'<h2>\1</h2>', h, flags=re.MULTILINE)

    # Bold
    h = re.sub(r'\*\*([^*]+)\*\*', r'<strong>\1</strong>', h)

    # Special boxes
    h = re.sub(r'\[WARNING\]([\s\S]*?)\[/WARNING\]',
               r'<div class="box-warning">\1</div>', h)
    h = re.sub(r'\[HIGHLIGHT\]([\s\S]*?)\[/HIGHLIGHT\]',
               r'<div class="box-highlight">\1</div>', h)
    h = re.sub(r'\[SUCCESS\]([\s\S]*?)\[/SUCCESS\]',
               r'<div class="box-success">\1</div>', h)

    # Lists
    h = re.sub(r'^- (.+)$', r'<li>\1</li>', h, flags=re.MULTILINE)
    h = re.sub(r'(<li>.*?</li>\n?)+', r'<ul>\g<0></ul>', h, flags=re.DOTALL)

    # Paragraphs / line breaks
    h = re.sub(r'\n\n', '</p><p>', h)
    h = re.sub(r'\n', '<br>', h)
    return f'<p>{h}</p>'


def build_html_email(content, generated_at=None):
    """Wrap investor insights content in a styled HTML email."""
    date_str = ''
